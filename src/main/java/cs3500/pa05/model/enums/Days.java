/**
 * enumeration representing the days of the week
 */
public enum Days {

  /**
   * represents sunday
   */
  SUNDAY,

  /**
   * represents monday
   */
  MONDAY,

  /**
   * represents tuesday
   */
  TUESDAY,

  /**
   * represents wednesday
   */
  WEDNESDAY,

  /**
   * represents thursday
   */
  THURSDAY,

  /**
   * represents friday
   */
  FRIDAY,

  /**
   * represents saturday
   */
  SATURDAY
}
